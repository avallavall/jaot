def get_email_string(email_key: str, string_key: str, locale: str | None = None) -> str:
    """Get a translated email string with English fallback.

    Args:
        email_key: Email identifier (day0, day1, day3, day7, day14, footer)
        string_key: String identifier within the email
        locale: Target locale (None or unknown falls back to English)

    Returns:
        Translated string, or English fallback if locale not found.
    """
    strings = EMAIL_TRANSLATIONS.get(email_key, {}).get(string_key, {})
    if not strings:
        return ""

    if locale and locale in strings:
        return strings[locale]

    # Fallback to English
    return strings.get("en", "")
